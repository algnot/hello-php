<?php
    // VARIABLE
    // ตัวแปรจะต้องมี $ อยู่ข้างหน้าชื่อตัวแปรทุกครั้ง
    // case-sensitive

    // ข้อความ
    $hello = "Hello world!";

    // จำนวนเต็ม
    $x = 5;

    // ทศนิยม
    $y = 10.5;

    // ค่าความจริง
    $z = TRUE;

    //สามารถเช็ค type โดยใช้คำสั่ง var_dump(ชื่อตัวแปร)
    echo var_dump($hello);
    echo "<br>";
    echo var_dump($x);
    echo "<br>";
    echo var_dump($y);
    echo "<br>";
    echo var_dump($z);
?>